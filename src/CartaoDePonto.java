public class CartaoDePonto {

    private int HoraChegada;
    private int MinutoChagada;
    private int SegundosChegada;

    private int HoraSaida;
    private int MinutoSaida;
    private int SegundosSaida;


    public int getHoraChegada() {
        return HoraChegada;
    }
    public void setHoraChegada(int horaChegada) {
        this.HoraChegada = horaChegada;
    }
    public int getMinutoChagada() {
        return MinutoChagada;
    }
    public void setMinutoChagada(int minutoChagada) {
        this.MinutoChagada = minutoChagada;
    }
    public int getSegundosChegada() {
        return SegundosChegada;
    }
    public void setSegundosChegada(int segundosChegada) {
        this.SegundosChegada = segundosChegada;
    }
    public int getHoraSaida() {
        return HoraSaida;
    }
    public void setHoraSaida(int horaSaida) {
        this.HoraSaida = horaSaida;
    }
    public int getMinutoSaida() {
        return MinutoSaida;
    }
    public void setMinutoSaida(int minutoSaida) {
        this.MinutoSaida = minutoSaida;
    }
    public int getSegundosSaida() {
        return SegundosSaida;
    }
    public void setSegundosSaida(int segundosSaida) {
        this.SegundosSaida = segundosSaida;
    }
}
